/**
 * Fill {{key}} placeholders in a template string with values from a params object.
 * Arrays are serialised to Python list literals.
 * Objects are serialised to Python dict literals.
 * Undefined/null values fall back to the placeholder default if one is embedded
 * as {{key|default}}, or to 'None'.
 */
function fillTemplate(template, params) {
  return template.replace(/\{\{(\w+)(?:\|([^}]*))?\}\}/g, (match, key, def) => {
    const raw = params[key] !== undefined && params[key] !== null ? params[key] : def;
    if (raw === undefined || raw === null) return 'None';

    if (Array.isArray(raw)) {
      const items = raw.map(v => (typeof v === 'string' ? `"${v}"` : String(v)));
      return `[${items.join(', ')}]`;
    }
    if (typeof raw === 'object') return JSON.stringify(raw);
    if (typeof raw === 'string') return raw;   // caller controls quoting in template
    return String(raw);
  });
}

