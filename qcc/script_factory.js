/**
 * Get orchestrator's addExperiment function.
 * The orchestrator may import the factory, or the factory may run standalone.
 * We use a lazy require to avoid circular imports.
 */
function _getOrchestratorHelpers() {
  // When the factory is imported by the orchestrator, addExperiment is passed
  // via init() as a second argument. If it wasn't, fall back to a direct DB insert.
  if (_addExperimentFn) return { addExperiment: _addExperimentFn };

  // Direct DB insert fallback (mirrors orchestrator's addExperiment logic)
  return {
    addExperiment: (data) => {
      const stmt = _db.db.prepare(`
        INSERT INTO research_queue (name, description, script_path, node, priority, config_json, depends_on, tags, max_retries)
        VALUES (@name, @description, @script_path, @node, @priority, @config_json, @depends_on, @tags, @max_retries)
      `);
      const result = stmt.run({
        name:        data.name,
        description: data.description || null,
        script_path: data.script_path,
        node:        data.node,
        priority:    data.priority || 5,
        config_json: data.config_json ? JSON.stringify(data.config_json) : null,
        depends_on:  data.depends_on || null,
        tags:        Array.isArray(data.tags) ? data.tags.join(',') : (data.tags || null),
        max_retries: data.max_retries !== undefined ? data.max_retries : 2,
      });
      return { id: Number(result.lastInsertRowid) };
    },
  };
}

