TEMPLATES.analysis = `#!/usr/bin/env python3
"""
Analysis script — generated by QCC Script Factory
Template: analysis

Params:
  analysis_type  : "ic_computation" | "pnl_breakdown" | "monte_carlo" | "all"
  input_dir      : directory containing prediction NPZ files or results JSON
  output_dir     : directory to write analysis output
  dates          : list of YYYYMMDD strings to include (empty = all in input_dir)
  mc_iterations  : Monte Carlo iterations for significance testing (default 1000)
  ic_lag         : forward lag for IC computation in bars (default 1)
  resample_freq  : PnL breakdown resampling frequency — "daily" | "weekly" | "monthly"
  model_name     : model identifier (for labelling output)
"""
import numpy as np
import json, os, sys, random
from pathlib import Path
from datetime import datetime

ANALYSIS_TYPE  = "{{analysis_type|all}}"
INPUT_DIR      = Path("{{input_dir}}")
OUTPUT_DIR     = Path("{{output_dir|.}}")
DATES          = {{dates|[]}}
MC_ITERATIONS  = {{mc_iterations|1000}}
IC_LAG         = {{ic_lag|1}}
RESAMPLE_FREQ  = "{{resample_freq|daily}}"
MODEL_NAME     = "{{model_name|unknown}}"

os.makedirs(OUTPUT_DIR, exist_ok=True)

# ── Load prediction files ─────────────────────────────────────────────────
npz_files = sorted(INPUT_DIR.glob("*.npz"))
if DATES:
    npz_files = [f for f in npz_files if any(d in f.name for d in DATES)]

print(f"[analysis] Found {len(npz_files)} NPZ files in {INPUT_DIR}", flush=True)

all_preds  = []
all_labels = []

for npz_file in npz_files:
    try:
        data   = np.load(str(npz_file), allow_pickle=True)
        preds  = data["predictions"] if "predictions" in data else data.get("signals", None)
        labels = data["labels"]      if "labels"      in data else data.get("targets", None)
        if preds is None or labels is None:
            print(f"  SKIP {npz_file.name}: missing predictions or labels", flush=True)
            continue
        all_preds.append(preds.flatten())
        all_labels.append(labels.flatten())
    except Exception as e:
        print(f"  ERROR loading {npz_file.name}: {e}", flush=True)

result = {
    "model_name":    MODEL_NAME,
    "analysis_type": ANALYSIS_TYPE,
    "n_files":       len(npz_files),
}

if not all_preds:
    print("[analysis] No data loaded — check input_dir and NPZ format", flush=True)
    print(f"RESULT: {json.dumps(result)}", flush=True)
    sys.exit(0)

preds  = np.concatenate(all_preds)
labels = np.concatenate(all_labels)
n      = len(preds)
print(f"[analysis] Loaded {n} prediction-label pairs", flush=True)

# ── IC Computation ────────────────────────────────────────────────────────
def compute_ic(p, l):
    """Pearson IC between predictions and lagged labels."""
    p_lag = p[:-IC_LAG]
    l_lag = l[IC_LAG:]
    if len(p_lag) < 2:
        return float("nan")
    corr = np.corrcoef(p_lag, l_lag)[0, 1]
    return float(corr) if np.isfinite(corr) else 0.0

if ANALYSIS_TYPE in ("ic_computation", "all"):
    ic = compute_ic(preds, labels)
    # Daily IC breakdown
    daily_ics = []
    for npz_file in npz_files[:30]:  # cap at 30 for speed
        try:
            d = np.load(str(npz_file), allow_pickle=True)
            p = d["predictions"] if "predictions" in d else d.get("signals", None)
            l = d["labels"]      if "labels"      in d else d.get("targets", None)
            if p is not None and l is not None:
                daily_ics.append(compute_ic(p.flatten(), l.flatten()))
        except Exception:
            pass

    result["ic"]             = round(ic, 6)
    result["daily_ic_mean"]  = round(float(np.nanmean(daily_ics)), 6)  if daily_ics else None
    result["daily_ic_std"]   = round(float(np.nanstd(daily_ics)),  6)  if daily_ics else None
    result["positive_days"]  = int(sum(x > 0 for x in daily_ics))      if daily_ics else None
    result["total_days"]     = len(daily_ics)
    print(f"  IC={ic:.4f}  daily_mean={result['daily_ic_mean']}  pos_days={result['positive_days']}/{result['total_days']}", flush=True)

# ── PnL Breakdown ─────────────────────────────────────────────────────────
if ANALYSIS_TYPE in ("pnl_breakdown", "all"):
    # Simplified PnL proxy: long when pred > 0, short when pred < 0
    signs   = np.sign(preds[:-IC_LAG])
    rets    = labels[IC_LAG:]
    pnl_per_bar = signs * rets

    # Remove zeros (flat bars)
    active  = pnl_per_bar[pnl_per_bar != 0]
    total_pnl    = float(np.sum(active))
    win_rate     = float(np.mean(active > 0)) if len(active) > 0 else 0.0
    avg_win      = float(np.mean(active[active > 0])) if np.any(active > 0) else 0.0
    avg_loss     = float(np.mean(active[active < 0])) if np.any(active < 0) else 0.0
    profit_factor = abs(avg_win / avg_loss) if avg_loss != 0 else float("inf")
    sharpe        = float(np.mean(active) / (np.std(active) + 1e-8) * np.sqrt(252 * 390)) if len(active) > 0 else 0.0

    result["pnl"]           = round(total_pnl, 4)
    result["win_rate"]      = round(win_rate, 4)
    result["avg_win"]       = round(avg_win, 6)
    result["avg_loss"]      = round(avg_loss, 6)
    result["profit_factor"] = round(profit_factor, 4) if np.isfinite(profit_factor) else None
    result["sharpe"]        = round(sharpe, 4)
    result["trades"]        = int(len(active))
    print(f"  pnl={total_pnl:.4f}  sharpe={sharpe:.3f}  win_rate={win_rate:.1%}  trades={len(active)}", flush=True)

# ── Monte Carlo ───────────────────────────────────────────────────────────
if ANALYSIS_TYPE in ("monte_carlo", "all") and "ic" in result:
    observed_ic = result["ic"]
    # Permutation test: shuffle labels MC_ITERATIONS times and recompute IC
    null_ics = []
    shuffled = labels.copy()
    for _ in range(MC_ITERATIONS):
        np.random.shuffle(shuffled)
        null_ics.append(compute_ic(preds, shuffled))

    null_ics = np.array(null_ics)
    p_value  = float(np.mean(null_ics >= observed_ic))
    result["mc_p_value"]       = round(p_value, 4)
    result["mc_iterations"]    = MC_ITERATIONS
    result["monte_carlo_passed"] = bool(p_value < 0.05)
    print(f"  Monte Carlo: p_value={p_value:.4f}  passed={result['monte_carlo_passed']}", flush=True)

# ── Save output ───────────────────────────────────────────────────────────
out_file = OUTPUT_DIR / f"analysis_{MODEL_NAME}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
with open(out_file, "w") as f:
    json.dump(result, f, indent=2)
print(f"[analysis] Results saved to {out_file}", flush=True)

print(f"RESULT: {json.dumps(result)}", flush=True)
`;

