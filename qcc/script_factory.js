/**
 * Pick the best node for a CPU-only analysis task.
 * Prefer Jupiter, then Neptune.
 */
function _pickCPUNode(currentNode) {
  if (_db) {
    try {
      const jupiter = _db.getNode('jupiter');
      if (jupiter && jupiter.status !== 'offline') return 'jupiter';
    } catch (_) {}
  }
  return currentNode || 'neptune';
}

