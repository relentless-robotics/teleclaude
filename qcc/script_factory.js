/**
 * List all available template names.
 */
function listTemplates() {
  return Object.keys(TEMPLATES);
}

/**
 * Generate a complete Python script string from a template name and parameter map.
 *
 * @param {string} templateName  — one of listTemplates()
 * @param {object} params        — key-value pairs matching {{placeholders}} in the template
 * @returns {string}             — fully rendered Python script
 */
function generateScript(templateName, params = {}) {
  const tpl = TEMPLATES[templateName];
  if (!tpl) {
    throw new Error(`Unknown template "${templateName}". Available: ${listTemplates().join(', ')}`);
  }
  return fillTemplate(tpl, params);
}

/**
 * Deploy a script to a remote node by writing it locally then uploading via
 * Python paramiko (reuses the same SFTP mechanism as the rest of the codebase).
 *
 * For Neptune (localhost / windows) we skip SFTP and write directly to the
 * local Lvl3Quant scripts directory.
 *
 * @param {string} scriptContent — Python source to upload
 * @param {string} nodeName      — target node (must exist in QCC DB or remote_servers.json)
 * @param {string} remotePath    — absolute path on the remote node where the file should land
 * @returns {string}             — the remotePath (confirmed)
 */
async function deployScript(scriptContent, nodeName, remotePath) {
  if (!_db) throw new Error('ScriptFactory.init(db) must be called before deployScript');

  // Ensure consistent line endings for the platform (Windows nodes prefer CRLF but Python handles LF fine)
  const normalized = scriptContent.replace(/\r\n/g, '\n');

  // ── Neptune / localhost: direct filesystem write ──────────────────────
  if (nodeName.toLowerCase() === 'neptune') {
    fs.writeFileSync(remotePath, normalized, 'utf8');
    console.log(`[ScriptFactory] Written directly to Neptune: ${remotePath}`);
    return remotePath;
  }

  // ── Remote nodes: SFTP via Python paramiko ───────────────────────────
  // Write the script to a temp local file, then upload via a Python SFTP helper.
  // We write the SFTP helper itself to a second temp file to avoid any shell
  // quoting / newline issues with -c inline scripts.
  const stamp    = Date.now();
  const tmpLocal = path.join(LOCAL_SCRIPTS, `_factory_payload_${stamp}.py`);
  const tmpSftp  = path.join(LOCAL_SCRIPTS, `_factory_sftp_${stamp}.py`);

  // Build path strings safe for Python raw-string literals (forward slashes)
  const pyBasePath  = BASE_DIR.replace(/\\/g, '/');
  const pyLocalSrc  = tmpLocal.replace(/\\/g, '/');
  const pyRemoteDst = remotePath.replace(/\\/g, '/');

  const sftpScript = [
    'import sys, os',
    `sys.path.insert(0, r"${pyBasePath}")`,
    'from utils.ssh_exec import load_config',
    'import paramiko',
    '',
    'cfg = load_config()',
    `srv = cfg["servers"].get("${nodeName}")`,
    'if not srv:',
    `    print("ERROR: server '${nodeName}' not in remote_servers.json", file=sys.stderr)`,
    '    sys.exit(1)',
    '',
    'host     = srv.get("tailscale_ip") or srv["host"]',
    'user     = srv["user"]',
    'passwd   = srv.get("password", "")',
    'port     = int(srv.get("port", 22))',
    'key_file = srv.get("key_file", "")',
    '',
    'client = paramiko.SSHClient()',
    'client.set_missing_host_key_policy(paramiko.AutoAddPolicy())',
    'kwargs = dict(hostname=host, port=port, username=user,',
    '              timeout=30, allow_agent=False, look_for_keys=False)',
    '',
    '# Prefer key auth when key_file is set and readable',
    'resolved_key = os.path.expanduser(key_file) if key_file else ""',
    'if resolved_key and os.path.exists(resolved_key):',
    '    try:',
    '        kwargs["pkey"] = paramiko.Ed25519Key.from_private_key_file(resolved_key)',
    '    except Exception:',
    '        try:',
    '            kwargs["pkey"] = paramiko.RSAKey.from_private_key_file(resolved_key)',
    '        except Exception:',
    '            kwargs["password"] = passwd',
    'else:',
    '    kwargs["password"] = passwd',
    '',
    'client.connect(**kwargs)',
    'sftp = client.open_sftp()',
    '',
    `local_src  = r"${pyLocalSrc}"`,
    `remote_dst = r"${pyRemoteDst}"`,
    '',
    '# Ensure remote parent directory exists (best-effort mkdir -p)',
    'remote_dir = remote_dst.replace("\\\\", "/").rsplit("/", 1)[0]',
    'def sftp_makedirs(sftp, d):',
    '    parts = d.lstrip("/").split("/")',
    '    cur = "/" if d.startswith("/") else ""',
    '    for p in parts:',
    '        if not p:',
    '            continue',
    '        cur = (cur + "/" + p).replace("//", "/")',
    '        try:',
    '            sftp.stat(cur)',
    '        except FileNotFoundError:',
    '            try:',
    '                sftp.mkdir(cur)',
    '            except Exception:',
    '                pass',
    'sftp_makedirs(sftp, remote_dir)',
    '',
    'sftp.put(local_src, remote_dst)',
    'sftp.close()',
    'client.close()',
    `print(f"OK: uploaded to ${nodeName}:{remote_dst}")`,
  ].join('\n');

  try {
    fs.writeFileSync(tmpLocal, normalized, 'utf8');
    fs.writeFileSync(tmpSftp,  sftpScript,  'utf8');

    const pyCmd = process.platform === 'win32' ? 'python' : 'python3';
    const result = execSync(
      `${pyCmd} "${tmpSftp}"`,
      { encoding: 'utf8', timeout: 60000, windowsHide: true }
    );
    console.log(`[ScriptFactory] SFTP upload: ${result.trim()}`);
    return remotePath;
  } finally {
    try { fs.unlinkSync(tmpLocal); } catch (_) {}
    try { fs.unlinkSync(tmpSftp);  } catch (_) {}
  }
}

/**
 * Resolve the remote absolute path for a generated script on a given node.
 *
 * Convention: <lvl3_root>/scripts/<scriptName>
 *
 * @param {string} scriptName  — filename (e.g. "fillsim_sweep_20250106.py")
 * @param {string} nodeName
 * @returns {string}
 */
function resolveRemotePath(scriptName, nodeName) {
  if (!_db) throw new Error('ScriptFactory.init(db) must be called before resolveRemotePath');

  const node = _db.getNode(nodeName);
  if (!node) throw new Error(`Node "${nodeName}" not found in QCC database`);

  const root    = node.lvl3_root;
  const isWin   = (node.os || '').toLowerCase() === 'windows';
  const sep     = isWin ? '\\' : '/';
  return `${root}${sep}${REMOTE_SCRIPTS_SUBDIR}${sep}${scriptName}`;
}

/**
 * Generate a descriptive script filename based on template and key params.
 */
function makeScriptName(templateName, params) {
  const ts    = new Date().toISOString().replace(/[:.]/g, '').slice(0, 15); // YYYYMMDDTHHmmss
  const label = params.model_name || params.strategy || templateName;
  const slug  = label.replace(/[^a-z0-9_]/gi, '_').toLowerCase();
  return `factory_${slug}_${ts}.py`;
}

/**
 * All-in-one: generate script, deploy to node, enqueue in research_queue.
 *
 * @param {string}  templateName  — template to use
 * @param {object}  params        — template parameters
 * @param {string}  nodeName      — target compute node
 * @param {number}  [priority=5] — queue priority (1=highest, 10=lowest)
 * @param {object}  [options]     — extra options:
 *   - dependsOn {number}         — research_queue ID this experiment depends on
 *   - tags {string[]}            — additional tags
 *   - description {string}       — human-readable description override
 *   - jobType {string}           — override job_type in config_json
 *   - requiresGpu {boolean}      — override requires_gpu
 *   - maxRetries {number}        — max retry count (default 2)
 *   - skipDeploy {boolean}       — skip SFTP upload (e.g. script already on node)
 *
 * @returns {Promise<number>}     — the new research_queue experiment ID
 */
async function createAndQueueExperiment(templateName, params = {}, nodeName, priority = 5, options = {}) {
  if (!_db) throw new Error('ScriptFactory.init(db) must be called before createAndQueueExperiment');

  // 1. Generate script
  const scriptContent = generateScript(templateName, params);

  // 2. Determine remote path
  const scriptName   = makeScriptName(templateName, params);
  const remotePath   = resolveRemotePath(scriptName, nodeName);

  // 3. Deploy (unless caller opts out)
  if (!options.skipDeploy) {
    await deployScript(scriptContent, nodeName, remotePath);
  }

  // 4. Derive job_type and requires_gpu from template if not overridden
  const TEMPLATE_JOB_TYPES = {
    fillsim_sweep:       { job_type: 'fillsim',  requires_gpu: false },
    strategy_signal_gen: { job_type: 'sweep',    requires_gpu: false },
    model_training:      { job_type: 'training', requires_gpu: true  },
    analysis:            { job_type: 'other',    requires_gpu: false },
  };
  const defaults = TEMPLATE_JOB_TYPES[templateName] || { job_type: 'other', requires_gpu: false };
  const jobType    = options.jobType    ?? defaults.job_type;
  const requiresGpu = options.requiresGpu ?? defaults.requires_gpu;

  // 5. Build default tags
  const baseTags = [templateName];
  if (params.model_name)  baseTags.push(params.model_name);
  if (params.strategy)    baseTags.push(params.strategy);
  if (options.tags && Array.isArray(options.tags)) baseTags.push(...options.tags);

  // 6. Build a readable name
  const label = params.model_name || params.strategy || templateName;
  const expName = `[factory] ${label} on ${nodeName}`;

  // 7. Enqueue
  const { addExperiment } = _getOrchestratorHelpers();
  const result = addExperiment({
    name:        expName,
    description: options.description || `Auto-generated ${templateName} experiment`,
    script_path: remotePath,
    node:        nodeName,
    priority,
    config_json: {
      job_type:     jobType,
      requires_gpu: requiresGpu,
      template:     templateName,
      params,
      // Pass params also as CLI args for simple scripts that accept --key value
      params: params,
    },
    depends_on:  options.dependsOn || null,
    tags:        baseTags,
    max_retries: options.maxRetries !== undefined ? options.maxRetries : 2,
  });

  console.log(`[ScriptFactory] Queued experiment #${result.id}: ${expName}`);
  return result.id;
}

/**
 * Suggest follow-up experiments based on a completed experiment's result.
 *
 * Returns an array of suggestion objects:
 *   { template, params, node, priority, dependsOn, reason }
 *
 * The caller decides whether to act on them.
 *
 * @param {object} completedExp  — research_queue row
 * @param {object} parsedResult  — parsed result JSON from that experiment
 * @returns {Array}
 */
function suggestFollowUp(completedExp, parsedResult) {
  const suggestions = [];
  if (!completedExp || !parsedResult) return suggestions;

  const config = completedExp.config_json
    ? (typeof completedExp.config_json === 'string'
        ? JSON.parse(completedExp.config_json)
        : completedExp.config_json)
    : {};
  const template = config.template || '';
  const params   = config.params   || {};
  const node     = completedExp.node;

  // ── Rule 1: strategy_signal_gen → fillsim_sweep ───────────────────────
  // If signal gen produced a meaningful number of signals, auto-queue a fill sim
  if (template === 'strategy_signal_gen') {
    const total = parsedResult.total_signals || 0;
    if (total >= 10000) {
      const outputPath = parsedResult.output_path;
      if (outputPath) {
        // Find fill_sim_cli path for the target node from remote_servers.json
        let fillSimCli = _getFillSimCli(node);
        suggestions.push({
          template:  'fillsim_sweep',
          node,
          priority:  completedExp.priority,   // same priority as parent
          dependsOn: completedExp.id,
          reason:    `signal_gen produced ${total} signals (≥10K threshold) — queue fill sim`,
          params: {
            dates:          params.dates || [],
            signal_sources: [outputPath],
            fill_sim_cli:   fillSimCli,
            output_dir:     outputPath.replace(/\.npz$/, '_fillsim'),
            tp_ticks:       [3, 4, 5, 6],
            sl_ticks:       [2, 3, 4],
            trailing_ticks: [0, 2],
            hold_ms:        [5000, 10000, 30000],
          },
        });
      }
    } else {
      // Low signal count — suggest re-running with a lower min_signal threshold
      const currentMin = parseFloat(params.min_signal || 0.01);
      if (currentMin > 0.001) {
        suggestions.push({
          template:  'strategy_signal_gen',
          node,
          priority:  completedExp.priority + 1,
          dependsOn: null,
          reason:    `only ${total} signals (<10K) — retry with lower min_signal`,
          params: {
            ...params,
            min_signal: +(currentMin / 2).toFixed(4),
          },
        });
      }
    }
  }

  // ── Rule 2: model_training → analysis (always after training) ─────────
  if (template === 'model_training') {
    const meanIC  = parsedResult.mean_ic || parsedResult.ic || null;
    const outDir  = parsedResult.output_dir || params.output_dir || null;

    if (outDir) {
      suggestions.push({
        template:  'analysis',
        node,
        priority:  completedExp.priority + 1,
        dependsOn: completedExp.id,
        reason:    `model training complete (mean_ic=${meanIC}) — run IC/PnL/MC analysis`,
        params: {
          analysis_type: 'all',
          input_dir:     outDir,
          output_dir:    outDir + '_analysis',
          model_name:    params.model_name || 'model',
          mc_iterations: 1000,
        },
      });
    }

    // Rule 2b: if IC improved substantially, also queue a fillsim sweep
    // (assuming predictions NPZ files are in output_dir)
    if (meanIC !== null && meanIC > 0.05) {
      const fillSimCli = _getFillSimCli(node);
      if (outDir) {
        suggestions.push({
          template:  'fillsim_sweep',
          node:      _pickFillSimNode(node),
          priority:  completedExp.priority + 2,
          dependsOn: completedExp.id,
          reason:    `IC=${meanIC.toFixed(4)} exceeds 0.05 threshold — validate with fill sim`,
          params: {
            dates:          params.dates || [],
            signal_sources: [outDir],  // fill_sim_cli can accept a dir of NPZ predictions
            fill_sim_cli:   fillSimCli,
            output_dir:     outDir + '_fillsim',
            tp_ticks:       [3, 4, 5],
            sl_ticks:       [2, 3],
            hold_ms:        [10000],
          },
        });
      }
    }
  }

  // ── Rule 3: fillsim_sweep → analysis ─────────────────────────────────
  // Always run a PnL breakdown analysis after a fill sim sweep
  if (template === 'fillsim_sweep') {
    const outDir = params.output_dir || null;
    if (outDir) {
      suggestions.push({
        template:  'analysis',
        node:      _pickCPUNode(node),
        priority:  completedExp.priority + 1,
        dependsOn: completedExp.id,
        reason:    `fillsim complete (${parsedResult.total_runs} runs) — run PnL breakdown`,
        params: {
          analysis_type: 'pnl_breakdown',
          input_dir:     outDir,
          output_dir:    outDir + '_analysis',
          model_name:    params.model_name || 'fillsim',
        },
      });
    }
  }

  // ── Rule 4: analysis with positive MC → re-queue fillsim at higher confidence ─
  if (template === 'analysis') {
    const mcPassed = parsedResult.monte_carlo_passed;
    const ic       = parsedResult.ic || 0;
    if (mcPassed && ic > 0.03) {
      suggestions.push({
        template:  'analysis',
        node:      _pickCPUNode(node),
        priority:  completedExp.priority,
        dependsOn: completedExp.id,
        reason:    `MC passed (p<0.05), IC=${ic} — re-run with more iterations for publication-quality stats`,
        params: {
          ...params,
          analysis_type:  'monte_carlo',
          mc_iterations:  10000,
          model_name:     (params.model_name || '') + '_highconf',
        },
      });
    }
  }

  return suggestions;
}

