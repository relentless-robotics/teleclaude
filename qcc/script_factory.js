TEMPLATES.strategy_signal_gen = `#!/usr/bin/env python3
"""
Strategy signal generator — generated by QCC Script Factory
Template: strategy_signal_gen
Generates trading signals from a configurable strategy and saves as NPZ.

Params:
  strategy       : strategy type — "book_imbalance" | "momentum" | "vwap_reversion" | "combined"
  data_dir       : directory containing MBO/book data (tick files, NPZ, etc.)
  output_path    : output NPZ file path (absolute)
  dates          : list of YYYYMMDD strings to generate signals for
  lookback_bars  : number of bars for momentum/smoothing window
  imbalance_depth: order book depth levels for imbalance calculation
  momentum_window: bars for momentum calculation
  min_signal     : minimum absolute signal value to keep (trim noise)
  normalize      : normalise signals to [-1, 1] range
  side           : "both" | "long_only" | "short_only"
"""
import numpy as np
import os, json, sys
from pathlib import Path

STRATEGY        = "{{strategy|book_imbalance}}"
DATA_DIR        = Path("{{data_dir}}")
OUTPUT_PATH     = Path("{{output_path}}")
DATES           = {{dates}}
LOOKBACK_BARS   = {{lookback_bars|20}}
IMBALANCE_DEPTH = {{imbalance_depth|5}}
MOMENTUM_WINDOW = {{momentum_window|10}}
MIN_SIGNAL      = {{min_signal|0.01}}
NORMALIZE       = {{normalize|True}}
SIDE            = "{{side|both}}"

os.makedirs(OUTPUT_PATH.parent, exist_ok=True)

def calc_book_imbalance(bids, asks, depth):
    """Compute bid-ask volume imbalance across depth levels."""
    bid_vol = np.sum(bids[:depth, 1]) if bids.shape[0] >= depth else np.sum(bids[:, 1])
    ask_vol = np.sum(asks[:depth, 1]) if asks.shape[0] >= depth else np.sum(asks[:, 1])
    total = bid_vol + ask_vol
    if total == 0:
        return 0.0
    return float((bid_vol - ask_vol) / total)

def calc_momentum(prices, window):
    """Simple price momentum: (price[t] - price[t-window]) / price[t-window]."""
    if len(prices) < window + 1:
        return 0.0
    return float((prices[-1] - prices[-window]) / (prices[-window] + 1e-8))

def calc_vwap_reversion(prices, volumes, window):
    """VWAP reversion signal: z-score of (price - VWAP)."""
    if len(prices) < window:
        return 0.0
    w_prices = np.array(prices[-window:], dtype=float)
    w_vols   = np.array(volumes[-window:], dtype=float)
    total_vol = np.sum(w_vols)
    if total_vol == 0:
        return 0.0
    vwap = np.sum(w_prices * w_vols) / total_vol
    std  = np.std(w_prices) + 1e-8
    return float(-(prices[-1] - vwap) / std)  # negative = fade the deviation

all_signals   = []
all_timestamps = []
total_signals = 0

for date in DATES:
    # Look for the date's data file — supports several naming conventions
    candidates = [
        DATA_DIR / f"{date}.npz",
        DATA_DIR / f"mbo_{date}.npz",
        DATA_DIR / f"book_{date}.npz",
        DATA_DIR / f"{date}" / "data.npz",
    ]
    data_file = next((p for p in candidates if p.exists()), None)

    if data_file is None:
        print(f"[signal_gen] WARNING: no data file for date {date} in {DATA_DIR}", flush=True)
        continue

    print(f"[signal_gen] Processing {date} from {data_file.name}", flush=True)
    data = np.load(str(data_file), allow_pickle=True)

    # Attempt to load standard arrays; fall back to zeros where missing
    prices    = data["prices"]    if "prices"    in data else data.get("mid", np.zeros(1))
    volumes   = data["volumes"]   if "volumes"   in data else np.ones(len(prices))
    bid_book  = data["bids"]      if "bids"      in data else None
    ask_book  = data["asks"]      if "asks"      in data else None
    timestamps = data["timestamps"] if "timestamps" in data else np.arange(len(prices))

    n = len(prices)
    signals = np.zeros(n, dtype=np.float32)

    for i in range(LOOKBACK_BARS, n):
        sig = 0.0

        if STRATEGY in ("book_imbalance", "combined") and bid_book is not None:
            bids_i = bid_book[i] if bid_book.ndim == 3 else bid_book
            asks_i = ask_book[i] if ask_book.ndim == 3 else ask_book
            sig += calc_book_imbalance(bids_i, asks_i, IMBALANCE_DEPTH)

        if STRATEGY in ("momentum", "combined"):
            sig += calc_momentum(prices[:i+1], MOMENTUM_WINDOW)

        if STRATEGY in ("vwap_reversion", "combined"):
            sig += calc_vwap_reversion(prices[:i+1], volumes[:i+1], LOOKBACK_BARS)

        signals[i] = sig

    # Apply side filter
    if SIDE == "long_only":
        signals = np.maximum(signals, 0)
    elif SIDE == "short_only":
        signals = np.minimum(signals, 0)

    # Normalize
    if NORMALIZE:
        max_abs = np.max(np.abs(signals)) + 1e-8
        signals = signals / max_abs

    # Threshold
    signals[np.abs(signals) < MIN_SIGNAL] = 0.0

    all_signals.append(signals)
    all_timestamps.append(timestamps)
    total_signals += int(np.sum(signals != 0))
    print(f"  {date}: {n} bars, {total_signals} signals generated", flush=True)

# Save combined output
if all_signals:
    combined_signals    = np.concatenate(all_signals,    axis=0)
    combined_timestamps = np.concatenate(all_timestamps, axis=0)
    np.savez_compressed(
        str(OUTPUT_PATH),
        signals=combined_signals,
        timestamps=combined_timestamps,
        dates=np.array(DATES, dtype="U10"),
        strategy=np.array([STRATEGY]),
    )
    print(f"[signal_gen] Saved {len(combined_signals)} rows to {OUTPUT_PATH}", flush=True)
else:
    print("[signal_gen] WARNING: no signals produced", flush=True)

result = {
    "total_signals": total_signals,
    "total_bars":    sum(len(s) for s in all_signals),
    "dates":         DATES,
    "strategy":      STRATEGY,
    "output_path":   str(OUTPUT_PATH),
}
print(f"RESULT: {json.dumps(result)}", flush=True)
`;

